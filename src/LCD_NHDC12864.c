/**
 * \brief LCD initialization
 * \param none
 * \return none
 */
void LCDInitializeNHDC12864(void)
{
	char i=0;
//	BitSet(P1OUT, LCD_RD);
	SysCtlDelay(16/3*300000);
	GPIOPinWrite(GPIO_PORTB_BASE, LCD_RD, LCD_RD);
	for(i=0;i<COMP_LCD_CONFIG_VECTOR;i++)
	{
		LCDWrite(COMM,lcdConfigVector[i]);
		//DelayUs(5);
		SysCtlDelay(16/3*5);
	}
	LCDClear();
}

